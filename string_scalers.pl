#!/usr/bin/perl

# In perl we can use dot(.) operator to concatinate the strings
print("Hey there,"."this is perl code\n");

# Also we can print perticular string N times repeatedly
print("Welcome "x4,"\n");

# Star/Asteric can be used for multiplication
# Perl automatically defines the type of a literal(String/Number) based on the operator applied
print(10*4,"\n");				# Here used asteric *, means it will print product of 10 & 4
print(10x4,"\n"); 				# Here used 'x' it means 10 will be printed 4 times
